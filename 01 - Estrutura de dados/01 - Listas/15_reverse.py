linguagens = ["python", "js", "c", "java", "csharp"]

linguagens.reverse()

print(linguagens)  # ["csharp", "java", "c", "js", "python"]


"""No Python, o método .reverse() é utilizado para inverter a ordem dos elementos de uma lista no próprio objeto (ou seja, modifica a lista original). Ele não retorna uma nova lista e sim None.

.reverse() inverte a lista original, sem criar uma nova.
Não retorna nada (None)."""